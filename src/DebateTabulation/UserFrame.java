    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        
         try {
            Class.forName("com.mysql.cj.jdbc.Driver");
           
            try (Connection conn = DriverManager.getConnection("jdbc:mysql://127.0.0.1:3306/userframe", "root", ""); Statement st = conn.createStatement()) {
                
                
                String query1="SELECT * FROM usernamepassword";
                ResultSet rs= st.executeQuery(query1);
                ResultSetMetaData rsmd= rs.getMetaData();
                DefaultTableModel model= (DefaultTableModel)tableData.getModel();
                int cols=rsmd.getColumnCount();
                String[] colName= new String[cols];
                for(int i=0;i<cols;i++)
                    colName[i]=rsmd.getColumnName(i+1);
                model.setColumnIdentifiers(colName);
                String name,name1;
                while(rs.next()){
                    name= rs.getString(1); name1=rs.getString(2);
                    String[] row={name,name1};
                    model.addRow(row);
                }
            }
                 
             } catch (SQLException | ClassNotFoundException ex) {
            Logger.getLogger(UserFrame.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

